export const Icon = ({
  name,
  size = 'md',
  color = 'currentColor',
  className = '',
  title,
  onClick,
  ...props
}: IconProps) => {
  const getSizeValue = (size: IconProps['size']): number => {
    if (typeof size === 'number') return size;
    
    const sizeMap = {
      xs: 12,
      sm: 16,
      md: 20,
      lg: 24,
      xl: 32,
    };
    
    return sizeMap[size] || 20;
  };

  const sizeValue = getSizeValue(size);
  const iconElement = iconMap[name];

  if (!iconElement) {
    console.warn(`Icon "${name}" not found in icon library`);
    return null;
  }

  const classes = [
    'icon',
    `icon--${size}`,
    onClick && 'icon--clickable',
    className,
  ].filter(Boolean).join(' ');

  return (
    <svg
      className={classes}
      width={sizeValue}
      height={sizeValue}
      viewBox="0 0 24 24"
      fill="none"
      stroke={color}
      strokeWidth="2"
      strokeLinecap="round"
      strokeLinejoin="round"
      onClick={onClick}
      role={onClick ? 'button' : 'img'}
      aria-label={title || name}
      title={title}
      {...props}
    >
      {iconElement}
    </svg>
  );
};
